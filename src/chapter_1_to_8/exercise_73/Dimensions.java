package chapter_1_to_8.exercise_73;

//wymiary
public class Dimensions {
    private int width;//szer
    private int height; //wys
    private int depth;

    public Dimensions(int width, int height, int depth) {
        this.width = width;
        this.height = height;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDepth() {
        return depth;
    }
}
